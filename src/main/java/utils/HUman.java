public class HUman {
    private int age;
    private String name;
    private String lastName;

    public int getAge() {
        return age;
    }

    public HUman setAge(int age) {
        this.age = age;
        return this;
    }

    public String getName() {
        return name;
    }

    public HUman setName(String name) {
        this.name = name;
        return this;
    }

    public String getLastName() {
        return lastName;
    }

    public HUman setLastName(String lastName) {
        this.lastName = lastName;
        return this;
    }

}
